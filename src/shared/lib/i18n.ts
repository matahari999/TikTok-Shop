export type TKey = keyof typeof translations['vi']

export function createT(lang: Lang) {
  const dict = translations[lang]
  return (key: TKey, params?: Record<string, string | number>): string => {
    let str: string = (dict as Record<string, string>)[key] ?? (translations.vi as Record<string, string>)[key] ?? key
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, String(v))
      })
    }
    return str
  }
}
